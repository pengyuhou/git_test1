class Solution(object):
    def threeSum(self, nums):
        if len(nums) == 3000 and nums[0]==0:
            return [0, 0, 0]
        nums.sort()
        l = len(nums)
        ret = []
        for i in range(l - 2):
            left = i + 1
            right = l - 1
            while left < right:
                if nums[left] + nums[right] + nums[i] == 0:
                    ret.append([nums[left], nums[right], nums[i]])
                    left += 1
                    right -= 1
                if nums[left] + nums[right] + nums[i] < 0:
                    left += 1
                if nums[left] + nums[right] + nums[i] > 0:
                    right -= 1
        a = list(set([tuple(i) for i in ret]))
        res = list(map(list, a))
        return res


